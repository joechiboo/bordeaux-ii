		1.各區分所有權人應按其建物登記總面積（不含停車位面積）計算以每坪每月<span class="highlight strikethrough">定額</span><span class="highlight">60元</span>分擔。 
		2.停車位以每一車位以每位每月<span class="highlight strikethrough">定額</span><span class="highlight">500元</span>分擔。
		<span class="highlight strikethrough">3.定額計算標準由區分所有權人會議決議訂定。</span>
